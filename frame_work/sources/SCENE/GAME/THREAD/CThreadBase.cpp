CThreadBase::CThreadBase(LPDIRECT3DDEVICE9 *pDevice, int priority, OBJTYPE type) : CScene2D(pDevice, priority, type)
{
	// 描画しない
	//SetDrawFlag(false);
	
	// 変数初期化
	m_nCount = 0;
}

//*****************************************************************************
// デストラクタ
//*****************************************************************************
CThreadBase ::~CThreadBase(void)
{
}

//*****************************************************************************
// 初期化
//*****************************************************************************
HRESULT CThreadBase::Init(D3DXVECTOR3 pos, float width, float height, TEXTURE_TYPE texType)
{

	CScene2D::Init(pos, width, height, texType);
	return S_OK;
}

//*****************************************************************************
// 終了
//*****************************************************************************
void CThreadBase::Uninit(void)
{
	CScene2D::Uninit();
}

//*****************************************************************************
// 更新
//*****************************************************************************
void CThreadBase::Update(void)
{
	m_nCount++;

	// 当たり判定フラグ更新
	if (m_nCount > m_nHitStartTime){
		m_bHitFlg = true;
	}
	if (m_nHitEndTime < m_nCount){
		m_bHitFlg = false;
	}
}

//*****************************************************************************
// 描画
//*****************************************************************************
